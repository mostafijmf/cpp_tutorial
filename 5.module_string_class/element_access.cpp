#include <bits/stdc++.h>
using namespace std;
int main()
{
    string s = "Hello World";
    cout << s[0] << endl; // access 0-th index of the string.
    cout << s.at(0) << endl; // access 0-th index of the string.
    cout << s.back() << endl; // access the last element of the string.
    cout << s.front() << endl; // access the first element of the string.

    return 0;
}
